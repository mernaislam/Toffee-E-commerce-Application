package ItemCollection;
/**
 * The status of an item in an e-commerce system.
 */
public enum ItemStatus
{
    /**
     * The item is currently on sale.
     */
    OnSale,
    /**
     * The item is not currently on sale.
     */
    NotOnSale,
    /**
     * The item is currently out of stock.
     */
    OutOfStock
}
